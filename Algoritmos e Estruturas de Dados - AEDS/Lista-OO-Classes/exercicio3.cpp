#include <iostream>

using namespace std;

class Carro
{

private:

    int tanque;
    int distancia;

public:

    Carro(int tan, int dist)  // construtor
    {

        setTanque(tan);
        setDistancia(dist);

    }

    setTanque(t)
    {

        this.tanque = t;

    }

    getTanque()
    {


        return this.tanque;

    }

    setDistancia(d)
    {

        this.distancia = d;

    }

    getDistancia()
    {


        return this.distancia;
    }






};
